#Length
a="Hello world"
print(len(a))

#Count
b="Hello World"
print(b.count("o"))

#UPPER
c="hello world"
print(c.upper())

#Lower
d="HELLO WORLD"
print(d.lower())

#index
e="Hello world"
print(e.index("H"))

#Capitalize
f="hello world"
print(f.capitalize())

#casefold
g="Hello world"
print(g.casefold())

#find
h="My name is krishna"
print(h.find("k"))

#center
name="John"
print(name.center(10))

#String slicing

a="Harry potter"
print(a[0:5])

b="0123456789"
